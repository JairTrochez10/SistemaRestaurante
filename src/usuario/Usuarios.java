        ContactoUser dao = new ContactoUser();

        int id = Integer.parseInt(txtId.getText());

        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este usuario?", "Confirmar", JOptionPane.YES_NO_OPTION);

